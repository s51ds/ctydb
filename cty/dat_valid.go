package cty

import (
	"errors"
	"strconv"
)

func (d Dat) Valid() error {
	if d.CountryName == "" {
		return errors.New("CountryName is empty")
	}
	if d.PrimaryPrefix == "" {
		return errors.New("PrimaryPrefix is empty")
	}
	if _, has := cqZoneMap[d.CqZone]; !has {
		return errors.New("invalid CQ zone:" + d.CqZone)
	}
	if _, has := ituZoneMap[d.CqZone]; !has {
		return errors.New("invalid ITU zone:" + d.CqZone)
	}
	if _, has := continentMap[d.Continent]; !has {
		return errors.New("invalid continent:" + d.Continent)
	}
	if _, err := strconv.ParseFloat(d.TimeOffset, 32); err != nil {
		return errors.New("invalid TimeOffset:" + d.TimeOffset)
	}
	return nil
}

